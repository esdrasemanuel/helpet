public class pojoR2Qtd_Cad {
    
    private String estado;
    private double qtd_usu;
    private double qtd_vet;
    private double qtd_ong;
    private double media;

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public double getQtd_usu() {
        return qtd_usu;
    }

    public void setQtd_usu(double qtd_usu) {
        this.qtd_usu = qtd_usu;
    }

    public double getQtd_vet() {
        return qtd_vet;
    }

    public void setQtd_vet(double qtd_vet) {
        this.qtd_vet = qtd_vet;
    }

    public double getQtd_ong() {
        return qtd_ong;
    }

    public void setQtd_ong(double qtd_ong) {
        this.qtd_ong = qtd_ong;
    }

    public double getMedia() {
        return media;
    }

    public void setMedia(double media) {
        this.media = media;
    }
    
    

}
